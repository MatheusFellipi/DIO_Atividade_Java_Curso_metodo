package atividad.src;
/**
 * calculadora
 */
public class Calculadora {

  public static void soma(double n1, double n2) {
    double resultados = n1 + n2;
    System.out.println("a soma de " + n1 + " + " + n2 + " = " + resultados);
  }

  public static void subtracao(double n1, double n2) {
    double resultados = n1 - n2;
    System.out.println("a subtracao de " + n1 + " - " + n2 + "=" + resultados);
  }

  public static void multiplicacao(double n1, double n2) {
    double resultados = n1 + n2;
    System.out.println("a multiplicacao de " + n1 + " * " + n2 + "=" + resultados);
  }

  public static void divisao(double n1, double n2) {
    double resultados = n1 + n2;
    System.out.println("a soma de " + n1 + " / " + n2 + "=" + resultados);
  }


}